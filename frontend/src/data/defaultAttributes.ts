/**
 * Gera um ID único para atributos locais
 */
export function generateAttributeId(): string {
  return `attr_${Date.now()}_${Math.random().toString(36).substr(2, 9)}`;
}

/**
 * Converte os atributos padrão para o formato completo com IDs
 */
export function getDefaultAttributesWithIds(): TechnicalAttribute[] {
  return DEFAULT_ATTRIBUTES.map((attr, index) => ({
    ...attr,
    id: `default_${attr.name}`,
    createdAt: new Date().toISOString(),
  }));
}
